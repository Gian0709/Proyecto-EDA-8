    private void cargarMovimientos(String dni) {
        System.out.println("Cargando movimientos para DNI: " + dni);
        
        DefaultTableModel modeloMovimientos = new DefaultTableModel();
        modeloMovimientos.addColumn("Movimiento");
        modeloMovimientos.addColumn("Observaci√≥n");
        modeloMovimientos.addColumn("Fecha");
        
        NodoMovimiento actual = gestionExp.obtenerMovimientoporDNI(dni);
        
        while (actual != null) {
            Movimiento mov = actual.getMovimiento();
            modeloMovimientos.addRow(new Object[]{
                mov.getNombre(),
                mov.getObservacion(),
                mov.getFechaRegistro()
            });
            actual = actual.getSiguiente();
        }
        
        jTableMovimientos.setModel(modeloMovimientos);
    }

